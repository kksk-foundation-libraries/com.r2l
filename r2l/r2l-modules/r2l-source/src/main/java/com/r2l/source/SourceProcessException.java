package com.r2l.source;

public class SourceProcessException extends RuntimeException {
	private static final long serialVersionUID = 1090282907231261273L;

	public SourceProcessException() {
		super();
	}

	public SourceProcessException(String message, Throwable cause) {
		super(message, cause);
	}

	public SourceProcessException(String message) {
		super(message);
	}

	public SourceProcessException(Throwable cause) {
		super(cause);
	}

}
